// Strings são uma cadeia de caracteres.

console.log ('olá') // aspas simples
console.log ("olá") // aspas duplas
console.log (`olá`) // crâses - você pode usar em bloco, pois é multilinha.

/* 
    Qual usar: aspas simples, aspas duplas ou crâses?

    - Quando você tiver que usar aspas simples dentro da cadeia de caracteres, usa-se aspas duplas.
    - Quando você tiver que usar aspas duplas dentro da cadeia de caracteres, usa-se aspas simples.
    - Quando você tiver que usar aspas simples e duplas dentro da cadeia de caracteres, usa-se aspas crâses.

    exemplo : 

    - ('Esse "programador" é um cara legal')
    - ("Esse 'programador' é um cara legal")
    - (`Esse 'programador' é um cara "legal", eu contrataria.`)
*/